// Rcon 동적 생성 함수 (GF(2^8)에서 2^(i-1) 계산)
void generate_rcon(uint8_t* rcon_array, int count) {
    uint8_t r = 1;
    rcon_array[0] = 0x00; // Rcon[0]은 사용 안 함
    for (int i = 1; i < count; i++) {
        rcon_array[i] = r;
        r = (r << 1) ^ ((r >> 7) * 0x1b); // xtime 연산
    }
}

// ======== AES 기본 연산 함수 정의 ======== //

// SubBytes: 바이트 단위 치환
static void SubBytes(uint8_t* state) {
    for (int i = 0; i < 16; i++)
        state[i] = sbox[state[i]];
}

// InvSubBytes: 역 치환
static void InvSubBytes(uint8_t* state) {
    for (int i = 0; i < 16; i++)
        state[i] = rsbox[state[i]];
}

// ShiftRows: 행 단위 왼쪽 시프트 (행 이동)
static void ShiftRows(uint8_t* state) {
    uint8_t tmp[16];
    tmp[0] = state[0];  tmp[1] = state[5];  tmp[2] = state[10]; tmp[3] = state[15];
    tmp[4] = state[4];  tmp[5] = state[9];  tmp[6] = state[14]; tmp[7] = state[3];
    tmp[8] = state[8];  tmp[9] = state[13]; tmp[10] = state[2]; tmp[11] = state[7];
    tmp[12] = state[12]; tmp[13] = state[1]; tmp[14] = state[6]; tmp[15] = state[11];
    memcpy(state, tmp, 16);
}

// InvShiftRows: 행 단위 오른쪽 시프트 (역행 이동)
static void InvShiftRows(uint8_t* state) {
    uint8_t tmp[16];
    tmp[0] = state[0];  tmp[1] = state[13]; tmp[2] = state[10]; tmp[3] = state[7];
    tmp[4] = state[4];  tmp[5] = state[1];  tmp[6] = state[14]; tmp[7] = state[11];
    tmp[8] = state[8];  tmp[9] = state[5];  tmp[10] = state[2]; tmp[11] = state[15];
    tmp[12] = state[12]; tmp[13] = state[9]; tmp[14] = state[6]; tmp[15] = state[3];
    memcpy(state, tmp, 16);
}

// xtime: GF(2^8)에서 x * 2 연산
static uint8_t xtime(uint8_t x) {
    return (x << 1) ^ ((x >> 7) * 0x1b);
}

// MixColumns: 열 단위 혼합
static void MixColumns(uint8_t* state) {
    for (int i = 0; i < 4; ++i) {
        uint8_t* col = &state[i * 4];
        uint8_t a = col[0], b = col[1], c = col[2], d = col[3];
        col[0] = xtime(a) ^ xtime(b) ^ b ^ c ^ d;
        col[1] = a ^ xtime(b) ^ xtime(c) ^ c ^ d;
        col[2] = a ^ b ^ xtime(c) ^ xtime(d) ^ d;
        col[3] = xtime(a) ^ a ^ b ^ c ^ xtime(d);
    }
}

// Galois Field에서 두 바이트 곱셈
static uint8_t gf_mul(uint8_t a, uint8_t b) {
    uint8_t result = 0;
    while (b) {
        if (b & 1) result ^= a;
        a = xtime(a);
        b >>= 1;
    }
    return result;
}

// InvMixColumns: 열 단위 역혼합
static void InvMixColumns(uint8_t* state) {
    for (int i = 0; i < 4; i++) {
        uint8_t* col = &state[i * 4];
        uint8_t a = col[0], b = col[1], c = col[2], d = col[3];
        col[0] = gf_mul(a,0x0e) ^ gf_mul(b,0x0b) ^ gf_mul(c,0x0d) ^ gf_mul(d,0x09);
        col[1] = gf_mul(a,0x09) ^ gf_mul(b,0x0e) ^ gf_mul(c,0x0b) ^ gf_mul(d,0x0d);
        col[2] = gf_mul(a,0x0d) ^ gf_mul(b,0x09) ^ gf_mul(c,0x0e) ^ gf_mul(d,0x0b);
        col[3] = gf_mul(a,0x0b) ^ gf_mul(b,0x0d) ^ gf_mul(c,0x09) ^ gf_mul(d,0x0e);
    }
}

// AddRoundKey: 라운드 키와 상태 배열 XOR
static void AddRoundKey(uint8_t* state, uint8_t* roundKey) {
    for (int i = 0; i < 16; i++)
        state[i] ^= roundKey[i];
}

// ======== 키 확장 ======== //

// AES 키 확장 함수
void KeyExpansion(AES_ctx* ctx, uint8_t* key) {
    int Nk = ctx->Nk;
    int Nr = ctx->Nr;
    int i, j;
    uint8_t temp[4], k;

    // Rcon 생성 (최대 15개까지 지원)
    generate_rcon(ctx->Rcon, 15);

    memcpy(ctx->roundKeys, key, 4 * Nk);  // 첫 Nk 워드 복사

    for (i = Nk; i < 4 * (Nr + 1); i++) {
        for (j = 0; j < 4; j++) temp[j] = ctx->roundKeys[(i - 1) * 4 + j];

        if (i % Nk == 0) {
            k = temp[0];
            temp[0] = sbox[temp[1]] ^ ctx->Rcon[i / Nk];
            temp[1] = sbox[temp[2]];
            temp[2] = sbox[temp[3]];
            temp[3] = sbox[k];
        } else if (Nk > 6 && i % Nk == 4) {
            for (j = 0; j < 4; j++) temp[j] = sbox[temp[j]];
        }

        for (j = 0; j < 4; j++) {
            ctx->roundKeys[i * 4 + j] = ctx->roundKeys[(i - Nk) * 4 + j] ^ temp[j];
        }
    }
    ctx->roundKeySize = 4 * (Nr + 1) * 4;
}

// ======== 블록 단위 암호화/복호화 ======== //

// AES 16바이트 블록 암호화
static void AES_encrypt_block(AES_ctx* ctx, uint8_t* input, uint8_t* output) {
    uint8_t state[16];
    memcpy(state, input, 16);

    AddRoundKey(state, ctx->roundKeys);  // 초기 라운드 키 추가

    for (int round = 1; round < ctx->Nr; round++) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, ctx->roundKeys + round * 16);
    }

    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, ctx->roundKeys + ctx->Nr * 16);

    memcpy(output, state, 16);
}

// AES 16바이트 블록 복호화
static void AES_decrypt_block(AES_ctx* ctx, uint8_t* input, uint8_t* output) {
    uint8_t state[16];
    memcpy(state, input, 16);

    AddRoundKey(state, ctx->roundKeys + ctx->Nr * 16);

    for (int round = ctx->Nr - 1; round > 0; round--) {
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(state, ctx->roundKeys + round * 16);
        InvMixColumns(state);
    }

    InvShiftRows(state);
    InvSubBytes(state);
    AddRoundKey(state, ctx->roundKeys);

    memcpy(output, state, 16);
}

// ======== CBC 모드 암호화/복호화 (PKCS7) ======== //

// AES CBC 모드 암호화
void AES_encrypt_CBC(AES_ctx* ctx, uint8_t* input, uint8_t* output, int length, uint8_t* iv) {
    int paddedLength = ((length / 16) + 1) * 16;  // 패딩 포함한 길이 계산
    uint8_t* paddedInput = (uint8_t*)malloc(paddedLength);
    uint8_t padding = paddedLength - length;

    memcpy(paddedInput, input, length);
    memset(paddedInput + length, padding, padding);  // PKCS7 패딩 추가

    uint8_t block[16];
    uint8_t prevBlock[16];
    memcpy(prevBlock, iv, 16);

    for (int i = 0; i < paddedLength; i += 16) {
        for (int j = 0; j < 16; j++) {
            block[j] = paddedInput[i + j] ^ prevBlock[j]; // CBC XOR
        }
        AES_encrypt_block(ctx, block, output + i);
        memcpy(prevBlock, output + i, 16);
    }
    free(paddedInput);
}

// AES CBC 모드 복호화
void AES_decrypt_CBC(AES_ctx* ctx, uint8_t* input, uint8_t* output, int length, uint8_t* iv) {
    uint8_t prev[16], block[16];
    memcpy(prev, iv, 16);

    for (int i = 0; i < length; i += 16) {
        AES_decrypt_block(ctx, input + i, block);
        for (int j = 0; j < 16; j++)
            output[i + j] = block[j] ^ prev[j]; // CBC XOR 복호화
        memcpy(prev, input + i, 16);
    }

    // 패딩 제거 (PKCS7)
    uint8_t padVal = output[length - 1];
    if (padVal > 0 && padVal <= 16) {
        int valid = 1;
        for (int i = 0; i < padVal; i++)
            if (output[length - 1 - i] != padVal) valid = 0;
        if (valid)
            memset(output + length - padVal, 0, padVal);
    }
}

// ======== 컨텍스트 초기화 함수 ======== //

// AES 컨텍스트 초기화: Nk, Nr 설정 및 키 확장
void AES_init_ctx(AES_ctx* ctx, uint8_t* key, int key_size_bits) {
    switch (key_size_bits) {
        case 128: ctx->Nk = 4; ctx->Nr = 10; break;
        case 192: ctx->Nk = 6; ctx->Nr = 12; break;
        case 256: ctx->Nk = 8; ctx->Nr = 14; break;
        default: printf("Unsupported key size\n"); exit(1);
    }
    KeyExpansion(ctx, key);
}
